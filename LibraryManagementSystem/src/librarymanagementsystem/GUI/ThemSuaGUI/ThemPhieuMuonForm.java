    private boolean check_inputTable(String maPM, String IDSach){
        if (maPM.equals("")){
            error_mess = "Mã Phiếu Mượn trống!!!";
            return false;
        }
        if (IDSach.equals("")){
            error_mess = "ID Sách trống!!!";
            return false;
        }
        if (dp.check_maMuonSach(maPM)!=true){
            error_mess = "Mã Phiếu Mượn nhập sai!!!";
            return false;
        }
        if (dp.check_maSach(IDSach)!=true){
            error_mess = "ID Sách nhập sai!!!";
            return false;
        }
        return true;
    }
    
    private boolean check_input(String maThe,String ngayMuon, String thoiGianMuon){
        if (maThe.equals("")){
            error_mess = "Mã Thẻ trống!!!";
            return false;
        }
        if (ngayMuon.equals("")){
            error_mess = "Ngày Mượn trống!!!";
            return false;
        }
        if (thoiGianMuon.equals("")){
            error_mess = "Thời Gian Mượn trống!!!";
            return false;
        }
        if (dp.check_maThe(maThe)!=true){
            error_mess = "Mã Thẻ nhập sai!!!";
            return false;
        }
        if (dp.check_number(thoiGianMuon)!=true){
            error_mess = "Thời Gian Mượn nhập sai!!!";
            return false;
        }
        
        return true;
    }
    
