    private ArrayList<String> getIDSach(){
        ArrayList<String> arrMS =new ArrayList<>();
        for(int i=0;i<tblCTPN.getRowCount();i++){
            String IDSach = (String) tblCTPN.getModel().getValueAt(i, 1);
            arrMS.add(IDSach);
        }
        return arrMS;
    }
        
    private void addButtonMouseClicked(java.awt.event.MouseEvent evt) {
        String maThe = maThe_Label.getText();
        String ngayMuon = ngayMuon_Label.getText();
        String thoiGianMuon = thoiGianMuon_Field.getText();
        String maPhieuMuon = dp.next_maMuonSach(pmBUS.getPKey());
        ArrayList<String> IDSach = getIDSach();
        if (check_input(maThe, ngayMuon, thoiGianMuon)){
            System.out.println("Nhập Thành Công");
            if (pmBUS.add(maPhieuMuon, maThe, IDSach, ngayMuon, Integer.parseInt(thoiGianMuon), "0000-00-00")){
                new AlertGUI(3, "Success", "Nhập Phiếu Nhập Thành Công!!!", "Quay Lại").setVisible(true);
                this.dispose();
            }
        }
        else{
            System.err.println(error_mess);
            System.out.println("Nhập Thất Bại");
            new AlertGUI(2, "Error", error_mess, "Quay Lại").setVisible(true);
            // Alert Form
        }
        
        System.out.println(maThe);
        System.out.println(ngayMuon);
        System.out.println(thoiGianMuon);
        
    }

    
