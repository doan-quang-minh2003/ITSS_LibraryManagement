    private void addButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addButtonMouseClicked
        //QLLoaiSachDTO loaiSach = new QLLoaiSachDTO();
        String tenSach = tenSach_textField.getText();
        String tenTacGia = tenTacGia_textField.getText();
        String tenNXB = tenNXB_textField.getText();
        String tenTheLoai = tenTheLoai_textField.getText();
        String ngonNgu = ngonNgu_textField.getText();
        String namXuatBan = namXuatBan_textField.getText();
        String giaTien = giaTien_textField.getText();
        String soTrang = soTrang_textField.getText();
        String tomTatNoiDung = tomTatNoiDung_textArea.getText();
        String maSach = ms;
        
        if (check_input(tenSach, tenTacGia, tenNXB, tenTheLoai, ngonNgu, namXuatBan, giaTien, soTrang, tomTatNoiDung, url)){
            int option = JOptionPane.showConfirmDialog(
                null, 
                "Bạn có muốn sửa " + tenSach + " ?", 
                "Xóa Loại Sách", 
                JOptionPane.YES_NO_OPTION);
            if(option == JOptionPane.YES_OPTION){
                System.out.println("Nhập Thành Công");
                if (loaisachBUS.mod(maSach, tenSach, tenTacGia, tenNXB, tenTheLoai, ngonNgu, tomTatNoiDung, Integer.parseInt(namXuatBan), Integer.parseInt(giaTien), Integer.parseInt(soTrang), url) ){
                    new AlertGUI(3, "Success", "Sửa Loại Sách Thành Công!!!", "Quay Lại").setVisible(true);
                    this.dispose();
                }
            }
        }
        else{
            System.err.println(error_mess);
            System.out.println("Sửa Thất Bại");
            new AlertGUI(2, "Error", error_mess, "Quay Lại").setVisible(true);
            // Alert Form
        }
        
        System.out.println(maSach);
        System.out.println(tenSach);
        System.out.println(tenTacGia);
        System.out.println(tenNXB);
        System.out.println(tenTheLoai);
        System.out.println(ngonNgu);
        System.out.println(namXuatBan);
        System.out.println(giaTien);
        System.out.println(soTrang);
        System.out.println(tomTatNoiDung);
        System.out.println(url);
        
    }//GEN-LAST:event_addButtonMouseClicked

