    private boolean check_input(String tenSach, String tenTacGia, String tenNXB, String tenTheLoai, String ngonNgu, String namXuatBan, String giaTien, String soTrang, String tomTatNoiDung, String hinhSach){
        if (tenSach.equals("")){
            error_mess = "Tên Sách trống!!!";
            return false;
        }
        else if (tenTacGia.equals("")){
            error_mess = "Tên Tác Giả trống!!!";
            return false;
        }
        else if (tenNXB.equals("")){
            error_mess = "Tên NXB trống!!!";
            return false;
        }
        else if (tenTheLoai.equals("")){
            error_mess = "Tên Thể Loại trống!!!";
            return false;
        }
        else if (ngonNgu.equals("")){
            error_mess = "Ngôn Ngữ trống!!!";
            return false;
        }
        else if (namXuatBan.equals("")){
            error_mess = "Năm XB trống!!!";
            return false;
        }
        else if (giaTien.equals("")){
            error_mess = "Giá Tiền trống!!!";
            return false;
        }
        else if (soTrang.equals("")){
            error_mess = "Số Trang trống!!!";
            return false;
        }
        else if (tomTatNoiDung.equals("")){
            error_mess = "Tóm Tắt Nội Dung trống!!!";
            return false;
        }
        else if (hinhSach.equals("")){
            error_mess = "Hình Sách trống!!!";
            return false;
        }
        
        else if (!dp.check_nam(namXuatBan)){
            error_mess = "Năm XB nhập sai!!!";
            return false;
        }
        else if (!dp.check_number(giaTien) && giaTien.length() > 15){
            error_mess = "Giá Tiền nhập sai!!!";
            return false;
        }
        else if (!dp.check_number(soTrang) && soTrang.length() > 15){
            error_mess = "Số Trang nhập sai!!!";
            return false;
        }
        
        return true;
    }
    
